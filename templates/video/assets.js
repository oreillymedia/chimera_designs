 $(document).ready(function(){
        $(".turnoff").click(function () {
        $("body").css({'background-color' : '#000'});
        });
      $(".soft").click(function () {
        $("body").css({'background-color' : '#4F4F4F'});
        });
      $(".turnon").click(function () {
        $("body").css({'background-color' : 'transparent'});
      });
    });




